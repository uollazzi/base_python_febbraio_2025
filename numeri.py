# numeri e operatori aritmetici
x = 5
y = 2

risultato = x + y # somma
risultato = x - y # sottrazione
risultato = x * y # moltiplicazione
risultato = x / y # divisione
risultato = x + y - 5 * 2

risultato = x % y # modulo (resto della divisione)
print(risultato)

risultato = x // y # quozionte
print(risultato)

risultato = x ** y # esponente (x alla y)
